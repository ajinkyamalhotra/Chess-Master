public class Move {

	int move = 0;
	int score = 0;
				
	public Move(int m) {
		this.move = m;
	}
		
	public void setScore(int s) {
		this.score = s;
	}
	public void setMove(int m) {
		this.move = m;
	}
	public int getScore() {
		return this.score;
	}
	public int getMove() {
		return this.move;
	}
}
	